function detectScheme(q) {
  const lq = q.toLowerCase();
  for (const s of SCHEME_MAP) {
    if (s.patterns.some(p => lq.includes(p))) return s.key;
  }
  return null;
}

function detectIntent(q) {
  const lq = q.toLowerCase();
  for (const i of INTENT_MAP) {
    if (i.patterns.some(p => lq.includes(p))) return i.intent;
  }
  return null;
}

function isAdviceQuestion(q) {
  const lq = q.toLowerCase();
  return ADVICE_KEYWORDS.some(k => lq.includes(k));
}

function isPIIQuestion(q) {
  const lq = q.toLowerCase();
  return PII_KEYWORDS.some(k => lq.includes(k));
}

function lookupFact(schemeKey, intent) {
  if (!intent) return null;
  if (schemeKey) {
    const scheme = KNOWLEDGE_BASE[schemeKey];
    if (scheme && scheme[intent]) return scheme[intent];
  }
  // Fall back to general
  if (KNOWLEDGE_BASE.general[intent]) return KNOWLEDGE_BASE.general[intent];
  return null;
}
